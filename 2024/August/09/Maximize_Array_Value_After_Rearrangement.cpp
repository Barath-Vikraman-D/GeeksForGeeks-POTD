class Solution {
  public:
    int Maximize(vector<int> &arr) {
        unsigned long int res =0,MOD=1e9+7,index;
        sort(arr.begin(),arr.end());
        for( index=0;index<arr.size();index++)
        {
            res = ((res %MOD) + (arr[index]*index)) % MOD;
        }
        return res%MOD;
    }
};

